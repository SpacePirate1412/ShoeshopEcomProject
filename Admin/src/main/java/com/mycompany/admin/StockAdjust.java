    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String sku = tfSku.getText();
        String name = tfName.getText();
        String brand = tfBrand.getText();
        String price = tfPrice.getText();
        String gender = (String) cbGender.getSelectedItem();
        String discount = tfDiscount.getText();
        String quantity = tfQuantity.getText();
        
        if(sku.isEmpty() || name.isEmpty() || brand.isEmpty() || price.isEmpty() || discount.isEmpty() || quantity.isEmpty()){
        JOptionPane.showMessageDialog(this, //if some text field is empty it will show the error text on the screen
                "Please enter all fields",
                "Try again",
                JOptionPane.ERROR_MESSAGE);
        } else{
            DefaultTableModel model = (DefaultTableModel) tableStock.getModel();
            model.addRow(new Object[]{ sku, name, brand, price, gender, discount, quantity});
            
            tfSku.setText(""); tfName.setText(""); tfBrand.setText(""); tfPrice.setText(""); tfDiscount.setText(""); tfQuantity.setText("");
            
        }
        
    }//GEN-LAST:event_btnAddActionPerformed


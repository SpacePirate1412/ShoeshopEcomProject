    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
       DefaultTableModel model = (DefaultTableModel) tableStock.getModel();
       Vector<Vector> tableData = model.getDataVector();
       
       //saving data in a file
       try{
           FileOutputStream stock = new FileOutputStream("stock.bin");
           ObjectOutputStream output = new ObjectOutputStream(stock);
           
           //method for serialization of object
           output.writeObject(tableData);
           
           output.close();
           stock.close();
       }catch (Exception ex){
           ex.printStackTrace();
       }
    }//GEN-LAST:event_formWindowClosing

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try{
            FileInputStream stock = new FileInputStream("stock.bin");
            ObjectInputStream input = new ObjectInputStream(stock);
            //method for deserialization of object
            Vector<Vector> tableData = (Vector<Vector>)input.readObject();
            
            input.close();
            stock.close();
            
            DefaultTableModel model = (DefaultTableModel) tableStock.getModel();
            for(int i = 0; i < tableData.size(); i++){
                Vector row = tableData.get(i);
                model.addRow(new Object[]{row.get(0), row.get(1), row.get(2), row.get(3), row.get(4), row.get(5), row.get(6)});
            }
        }catch (Exception ex){
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_formWindowOpened

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = tableStock.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(this,
                    "No row is selected! Please select one row",
                    "Select row",
                    JOptionPane.ERROR_MESSAGE);
        }else {
            DefaultTableModel model = (DefaultTableModel) tableStock.getModel();
            model.removeRow(row);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

